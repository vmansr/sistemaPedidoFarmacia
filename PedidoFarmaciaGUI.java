    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
       String nombre = txtNombreMedicamento.getText();
        String tipo = (String) cbTipoMedicamento.getSelectedItem();
        String cantidadTexto = txtCantidad.getText();
        String distribuidor = null;

    if (rbCofarma.isSelected()) {
        distribuidor = "Cofarma";
    } else if (rbEmpsephar.isSelected()) {
        distribuidor = "Empsephar";
    } else if (rbCemefar.isSelected()) {
        distribuidor = "Cemefar";
    }

    // Validaciones
    if (nombre.isEmpty() || !nombre.matches("[a-zA-Z0-9 ]+")) {
        JOptionPane.showMessageDialog(this, "Debe ingresar un nombre de medicamento válido.");
        return;
    }
    if (cantidadTexto.isEmpty() || !cantidadTexto.matches("\\d+") || Integer.parseInt(cantidadTexto) <= 0) {
        JOptionPane.showMessageDialog(this, "Debe ingresar una cantidad válida.");
        return;
    }
    if (distribuidor == null) {
        JOptionPane.showMessageDialog(this, "Debe seleccionar un distribuidor.");
        return;
    }
    if (!chkPrincipal.isSelected() && !chkSecundaria.isSelected()) {
        JOptionPane.showMessageDialog(this, "Debe seleccionar al menos una sucursal.");
        return;
    }

    int cantidad = Integer.parseInt(cantidadTexto);
    String sucursales = "";
    if (chkPrincipal.isSelected()) sucursales += "Calle de la Rosa No. 28";
    if (chkSecundaria.isSelected()) sucursales += "Calle Alcazabilla No.3";

    mostrarResumen(nombre, tipo, cantidad, distribuidor, sucursales); // TODO add your handling code here:
    }//GEN-LAST:event_btnConfirmarActionPerformed

