     private void mostrarResumen(String nombre, String tipo, int cantidad, String distribuidor, String sucursales) {
        JFrame resumen = new JFrame("Pedido al distribuidor " + distribuidor);
        resumen.setSize(600, 200);
    
        // Cambia GridLayout por FlowLayout o BoxLayout para mayor flexibilidad
        resumen.setLayout(new FlowLayout());

        JLabel lblPedido = new JLabel(cantidad + " unidades del " + tipo + " " + nombre);
        JLabel lblSucursal = new JLabel("para la farmacia situada en la " + sucursales);

        JButton btnEnviar = new JButton("Enviar Pedido");
        btnEnviar.addActionListener((ActionEvent e) -> {
        System.out.println("Pedido enviado");
        resumen.dispose();
    });

    JButton btnCancelar = new JButton("Cancelar");
    btnCancelar.addActionListener((ActionEvent e) -> {
        resumen.dispose();
    });

    resumen.add(lblPedido);
    resumen.add(lblSucursal);
    JPanel panelBotones = new JPanel();
    panelBotones.add(btnEnviar);
    panelBotones.add(btnCancelar);
    resumen.add(panelBotones);

    resumen.setVisible(true);
}
     
     private void borrarFormulario(){
         txtNombreMedicamento.setText("");
         cbTipoMedicamento.setSelectedIndex(0);
         txtCantidad.setText("");
         rbCofarma.setSelected(false);
         rbEmpsephar.setSelected(false);
         rbCemefar.setSelected(false);
         chkPrincipal.setSelected(false);
         chkSecundaria.setSelected(false);
     }
